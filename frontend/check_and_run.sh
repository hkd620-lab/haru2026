#!/bin/bash

echo "허교장님, 파이어베이스 연결 암호 검사를 시작합니다..."

# .env 파일에 '여기에_' 라는 임시 글자가 여전히 남아있는지 확인합니다.
if grep -q "여기에_" .env; then
    echo "❌ 삐빅! 경고: .env 파일에 아직 허교장님의 진짜 값이 입력되지 않았습니다."
    echo "----------------------------------------------------"
    echo "[해결 방법]"
    echo "1. 코드 편집기(VS Code 등)로 현재 폴더의 '.env' 파일을 엽니다."
    echo "2. 파이어베이스 웹사이트 [프로젝트 설정(톱니바퀴 아이콘)] ➡️ [일반] 탭 하단에 있는 영문/숫자 코드들로 빈칸을 채워 넣고 저장하세요."
    echo "3. 저장이 끝나면 터미널에서 다시 './check_and_run.sh' 를 실행하세요."
    echo "----------------------------------------------------"
else
    echo "✅ .env 파일 값이 채워진 것을 확인했습니다!"
    echo "새로운 암호를 적용하여 서버를 재시작합니다..."
    npm run dev
fi
