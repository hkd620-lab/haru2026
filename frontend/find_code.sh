#!/bin/bash

echo "허교장님, '오늘의 기록' 화면 코드가 있는 파일을 찾습니다..."

# "오늘의 기록"이라는 글자가 포함된 파일을 src 폴더 안에서 찾습니다.
TARGET_FILE=$(grep -rl "오늘의 기록" src/ | grep -E "\.(jsx|tsx|js)$" | head -n 1)

if [ -n "$TARGET_FILE" ]; then
    echo "✅ 성공적으로 파일을 찾았습니다: $TARGET_FILE"
    echo "아래 절취선 안의 코드 전체를 드래그해서 복사한 뒤, 저(HARU)에게 채팅으로 보여주십시오."
    echo "======================= 코드 시작 ======================="
    cat "$TARGET_FILE"
    echo "======================= 코드 끝 ======================="
else
    echo "❌ 안타깝게도 해당 파일을 찾지 못했습니다."
fi
