#!/bin/bash

# 허교장님을 위한 디자인 성공 상태 기록(Git) 스크립트

echo "현재의 훌륭한 디자인 상태를 안전하게 기록합니다..."

# 1. 버전 관리 시스템(Git)이 있는지 확인하고 없으면 시작합니다.
if [ ! -d ".git" ]; then
    echo "🌱 버전 관리를 처음 시작합니다."
    git init
fi

# 2. 변경된 모든 디자인 파일들을 저장 준비 상태로 올립니다.
git add .

# 3. '피그마 로그인 디자인 적용 완료'라는 메모와 함께 확정 지어 저장합니다.
git commit -m "완료: 피그마 로그인 화면 디자인 성공적 적용"

echo "✅ 기록이 완벽하게 끝났습니다, 허교장님. 이제 코드가 꼬이더라도 언제든 지금 상태로 돌아올 수 있습니다."
